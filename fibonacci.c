void setRotation()
{
    for (int x = 99; x != -1; x--)
    {
        if (tempRotation[x] >= 10)
        {
            tempRotation[x] -= 10;
            if (tempRotation[x - 1] == -1)
            {
                tempRotation[x - 1] += 2;
            }
            else
            {
                tempRotation[x - 1]++;
            }
        }
        if (tempRotation[x] == -1)
        {
            break;
        }
    }

}

void moveTempToInput(int targetArr)
{
    for (int x = 99; x != -1; x--)
    {
        if (tempRotation[x] != -1)
        {
            input[targetArr][x] = tempRotation[x] + 48;
        }
        else
        {
            break;
        }
    }
}

bool resetNotCompleted()
{
    for (int x = 99; x != -1; x--)
    {
        if (tempRotation[x] >= 10)
        {
            return true;
        }
        if (tempRotation[x] == -1)
        {
            break;
        }
    }
    return false;
}

void addTheTwo()
{
    for (int j = 99; j != -1; j--)
    {
        if (input[0][j] != '\0' && input[1][j] != '\0')
        {
            tempRotation[j] = input[0][j] - 48 + input[1][j] - 48;
        }
        else
        {
            break;
        }
    }
}

void printIt(int targetArr)
{
    for (int x = 0; x < 100; x++)
    {
        if (input[targetArr][x] != '\0')
        {
            printf("%c", input[targetArr][x]);
        }
    }
    printf("\n");
}